                                <br>
                                <p data-aos="fade-up">The Livestock Field Station, which is managed by the Department of Animal Science, is located in Mawelawatta, Uda Peradeniya about 2 Km away from Peradeniya.</p>
                                <p data-aos="fade-up">Though the livestock farm facilities were initiated at the department in 1968, such were limited to the poultry and a hatchery located at the Department of Animal Science premises and a swine unit located at Uda-Peradeniya. Subsequently, in 1968/69, 46 acres were allocated to the department from the Dodangolla farm to establish a full-fledged Livestock Farm and it was completed in 1971. Farm facilities provided in Dodangolla, 16 miles away from the department were difficult to be utilizes for intended purposes of teaching undergraduates. Hence, in 1979 development activities were initiated in the university at Uda-Peradeniya.</p>
                                <p data-aos="fade-up">Sales Unit of the Department of Animal Science was established in 1972 with the sole purpose of providing fresh farm products among the university community as well as the other consumers in the area. At the beginning the sales unit was located in old Animal Science building premises and functioned in a small scale. Since it was a more service oriented function than profit generating venture, most of the products were provided at a lower price than the normal market price. The unit also catered as an extension arm in popularizing farm products among the university community and also plays a role in developing healthy food consumption pattern. With the expansion of the facilities available at Mawela Livestock Field Station, value added/ processed products were also introduced.</p>
                                <p data-aos="fade-up">The Livestock Field Station is maintained as a model farm to demonstrate livestock-bio energy-fish integration system. The total extent of farm is 100 ha. The field station includes production units of various breeds of cattle, buffalo, sheep, goats, rabbits, poultry, broilers, quails, ducks, swine and a micro livestock production division, which are maintained by the Department of Animal Science for undergraduate and postgraduate teaching, research and training in Animal Production.</p>
                                <p data-aos="fade-up">At present, the Livestock Field Station at Uda-Peradeniya is managed by a farm manager, an assistant farm manager, one field supervisor, two dairy assistants, one farm supervisor and one salesman. In addition, there are 17 laborers and 2 drivers as well to perform day-to-day activities.</p>
                                <p data-aos="fade-up">The Livestock Field Station is consisted of several units which provide various fresh farm products.</p>
