                                <p data-aos="fade-up">In order to provide a better service for the consumers, Sales Unit was shifted from its former location to a new location at Old Galaha road adjacent to the new administration complex in July 2007. Its new establishment got a huge consumer attraction and in order to fulfill the demand, the capacity of the unit and the range of products sold were increased. As value added products Yoghurt, Ice Cream cup and cone, milk toffee, hot milk, cold milk, flavored milk, cheese and barbeque have been introduced. In addition to its routine sales activities, the sales unit accepts orders in festival seasons. At present the Sales Unit is managed by a revolving fund under the Department of Animal Science.</p>
                                <p data-aos="fade-up">Various kinds of fresh farm produce and processed dairy and meat products are sold to the surrounding community, at the Agro-Product Sales Centre located within the faculty of Agriculture premises and at the University Milk Bar located near the Faculty of Arts. Both of these sales outlets are managed by the Department of Animal Science as a service. The Department has been expanding its processing and sales activities with the recent establishment of Dairy Product Processing Centre at the Livestock Field station. In addition a milk collection network has been established involving dairy farmers in the surrounding areas, whose milk is collected daily at a relatively higher price, processed at the processing centre and sold at the above sales outlets.</p>
